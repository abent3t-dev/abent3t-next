function NavItemComponent({ item, level = 0 }: { item: NavItem; level?: number }) {
  const pathname = usePathname();
  const { user } = useAuth();
  const [isOpen, setIsOpen] = useState(pathname.startsWith(item.href));

  // Verificar si el usuario tiene permiso para ver este item
  if (!user || !item.roles.includes(user.role)) {
    return null;
  }

  const Icon = Icons[item.icon] || Icons.database;
  const isActive = pathname === item.href || (item.children && pathname.startsWith(item.href));
  const hasChildren = item.children && item.children.length > 0;

  // Filtrar hijos que el usuario puede ver
  const visibleChildren = item.children?.filter((child) => child.roles.includes(user.role)) || [];

  if (hasChildren && visibleChildren.length === 0) {
    return null;
  }

  return (
    <div>
      {hasChildren ? (
        <button
          onClick={() => setIsOpen(!isOpen)}
          className={`w-full flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
            isActive
              ? 'bg-blue-50 text-blue-700'
              : 'text-gray-700 hover:bg-gray-100'
          }`}
        >
          <Icon className="w-5 h-5 flex-shrink-0" />
          <span className="flex-1 text-left">{item.label}</span>
          <Icons.chevron
            className={`w-4 h-4 transition-transform ${isOpen ? 'rotate-180' : ''}`}
          />
        </button>
      ) : (
        <Link
          href={item.href}
          className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
            isActive
              ? 'bg-blue-50 text-blue-700'
              : 'text-gray-700 hover:bg-gray-100'
          } ${level > 0 ? 'pl-10' : ''}`}
        >
          <Icon className="w-5 h-5 flex-shrink-0" />
          <span>{item.label}</span>
        </Link>
      )}

      {hasChildren && isOpen && (
        <div className="mt-1 space-y-1">
          {visibleChildren.map((child) => (
            <NavItemComponent key={child.href} item={child} level={level + 1} />
          ))}
        </div>
      )}
    </div>
  );
}

export function Sidebar() {
  const { user, signOut } = useAuth();

  return (
    <aside className="w-64 bg-white border-r border-gray-200 flex flex-col h-screen">
      {/* Logo / Header */}
      <div className="p-4 border-b border-gray-200">
        <h1 className="text-xl font-bold text-gray-900">Abent 3T</h1>
        <p className="text-xs text-gray-500">Sistema de Gestión</p>
      </div>

      {/* User Info */}
      {user && (
        <div className="p-4 border-b border-gray-200 bg-gray-50">
          <p className="font-medium text-gray-900 truncate">{user.full_name}</p>
          <p className="text-xs text-gray-500">{ROLE_LABELS[user.role]}</p>
          {user.departments && (
            <p className="text-xs text-blue-600 mt-1">{user.departments.name}</p>
          )}
        </div>
      )}

      {/* Navigation */}
      <nav className="flex-1 p-4 space-y-2 overflow-y-auto">
        {SIDEBAR_NAV.map((item) => (
          <NavItemComponent key={item.href} item={item} />
        ))}
      </nav>

      {/* Logout */}
      <div className="p-4 border-t border-gray-200">
        <button
          onClick={signOut}
          className="w-full flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium text-red-600 hover:bg-red-50 transition-colors"
        >
          <Icons.logout className="w-5 h-5" />
          <span>Cerrar Sesión</span>
        </button>
      </div>
    </aside>
  );
}
